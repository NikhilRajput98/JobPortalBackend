import mongoose from "mongoose";

const otpSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  otp: {
    type: String,
    required: true,
  },
  expiresAt: {
    type: Date,
    required: true,
  },
  isUsed: {
    type: Boolean,
    default: false,
  },
  companyId: { 
    type: mongoose.Schema.Types.ObjectId, 
    ref: "Company" 
  },
},{timestamps: true});

export default mongoose.model("Otp", otpSchema);
